    private void inicializaTabela() {
        //Apaga os elementos da tabela
        DefaultTableModel dtm = (DefaultTableModel) jtCliente.getModel();
        while (dtm.getRowCount() > 0)
            dtm.removeRow(0);
        for (Cliente vet : getListaCliente()) {
            addClienteNaTabela(vet);
        }
    }

    private void addClienteNaTabela(Cliente cli) {

        DefaultTableModel dtm = (DefaultTableModel) jtCliente.getModel();

        Object dados[] = {cli.getId(),cli.getNome(), cli.getTelefone(), cli.getEmail(), AnimalDAO.getInstance().getAllAnimais(cli.getId()).size()};
        dtm.addRow(dados);
    }

