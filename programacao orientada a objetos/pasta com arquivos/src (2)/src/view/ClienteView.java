    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        int id = ClienteDAO.getInstance().addCliente(txtNome.getText(),
                txtCep.getText(),
                txtEmail.getText(),
                txtTelefone.getText(),
                txtRua.getText(),
                txtNumero.getText(),
                txtComplemento.getText(),
                txtBairro.getText(),
                txtCidade.getText(),
                (String) jcbEstado.getSelectedItem());
        if (id >= 0) {
            addClienteNaTabela(ClienteDAO.getInstance().getClienteById(id));
            txtNome.setText("");
            txtCep.setText("");
            txtEmail.setText("");
            txtTelefone.setText("");
            txtRua.setText("");
            txtNumero.setText("");
            txtComplemento.setText("");
            txtBairro.setText("");
            txtEmail.setText("");
            txtCidade.setText("");
        }
    }//GEN-LAST:event_jbCadastrarActionPerformed

